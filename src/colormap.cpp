QVector<QRgb> turbo(int n)
{
    QVector<QRgb> map(n);
    for (int i = 0; i < n; ++i) {
        float v = 1.0f * i / n;
        int p = std::floor(v * 256);
        const float *q = _turbo_ + p * 3;
        map[i] = qRgb(*q * 255, *(q + 1) * 255, *(q + 2) * 255);
    }
    return map;
}
