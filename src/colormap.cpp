QVector<QRgb> viridis(int n);
QVector<QRgb> turbo(int n);
QVector<QRgb> gray(int n);
QVector<QRgb> jet(int n);

QVector<QRgb> QMatPlotWidget::colorMap(ColorMapType t, int n)
{
    switch (t) {
    case Gray:
        return gray(n);
    case Viridis:
        return viridis(n);
    case Turbo:
        return turbo(n);
    case Jet:
        return jet(n);
    }
    return QVector<QRgb>();
}

